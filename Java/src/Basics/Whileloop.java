package Basics;

public class Whileloop {

	public static void main(String[] args) {
		 //WHILE LOOP
		
		int i =1;
		while(i<=4) {
			System.out.println("Parimala " + i);
			i++;
		}
		System.out.println("Tanuja " + i);
		
		
		
		
		// Nested loop
		
		 int days=1;
		 while(days<=7) {
		   System.out.println("Day " + days);
		 int hours = 1;
		   while(hours<= 12){
			   System.out.println("Hour " + hours);
			   hours++;
			   }
		   days++;
		   }

		 
	 
		 
	}

}
